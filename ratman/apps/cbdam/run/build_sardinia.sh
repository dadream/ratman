CBDAM_HEIGHT_SUBSAMPLING=0
CBDAM_INPUT_HEIGHT_FIELD=/data1/cbdam/img/sardinia/regional/dem/w001001.adf
CBDAM_INPUT_HEIGHT_FIELD_HEIGHT_SCALE=1
CBDAM_INPUT_HEIGHT_FIELD_SAMPLE_SPACING=10
#~500 KM ,524289m = 512*1024+1
CBDAM_HEIGHT_U0=0.359314233180555
CBDAM_HEIGHT_V0=0.237720989759465
CBDAM_HEIGHT_U1=0.640685766819445
CBDAM_HEIGHT_V1=0.762279010240535
#300km
#CBDAM_HEIGHT_U0=0.2541333333
#CBDAM_HEIGHT_V0=0.0416333333
#CBDAM_HEIGHT_U1=0.7458666667
#CBDAM_HEIGHT_V1=0.9583666667
#cagliari quad
#CBDAM_HEIGHT_U0=-0.4834666667
#CBDAM_HEIGHT_V0=-0.4167333333
#CBDAM_HEIGHT_U1=1.48346666667
#CBDAM_HEIGHT_V1=3.25020000000

CBDAM_TMP_DIR=/tmp/cbdam_tmp/
CBDAM_OUTPUT_HEIGHT_WAVELET_ALPHA=0.5
CBDAM_OUTPUT_HEIGHT_FIELD_TOLERANCE=1
CBDAM_OUTPUT_HEIGHT_PATCH_DIM=32
CBDAM_OUTPUT_HEIGHT_FIELD="/data1/cbdam/data/new_formats/quad/height_sardinia_amax_tol"$CBDAM_OUTPUT_HEIGHT_FIELD_TOLERANCE"_sub"$CBDAM_HEIGHT_SUBSAMPLING"_alpha"$CBDAM_OUTPUT_HEIGHT_WAVELET_ALPHA"_patchdim"$CBDAM_OUTPUT_HEIGHT_PATCH_DIM
CBDAM_OUTPUT_HEIGHT_FIELD_LOG=$CBDAM_OUTPUT_HEIGHT_FIELD".log"

CBDAM_INPUT_COLOR_FIELD=/data1/cbdam/img/sardinia/regional/ortho/mosaico_2000IT.ecw
#CBDAM_INPUT_COLOR_FIELD=/data1/cbdam/img/sardinia/regional/ortho/mosaico10m.tif
#~500 KM ,524289m = 512*1024+1
#CBDAM_COLOR_U0=0.354105579174844
#CBDAM_COLOR_V0=0.237712082458339
#CBDAM_COLOR_U1=0.637302441973797
#CBDAM_COLOR_V1=0.767257810100918
#300 km
#CBDAM_COLOR_U0=0.245030533
#CBDAM_COLOR_V0=0.041617767
#CBDAM_COLOR_U1=0.739953867
#CBDAM_COLOR_V1=0.967067767
#cagliari quad
CBDAM_COLOR_U0=-0.51987787
CBDAM_COLOR_V0=-0.41679560
CBDAM_COLOR_U1=1.45981547
CBDAM_COLOR_V1=3.28500440

CBDAM_COLOR_SUBSAMPLING=0
CBDAM_OUTPUT_COLOR_WAVELET_ALPHA=0.5
CBDAM_OUTPUT_COLOR_FIELD_TOLERANCE=2
CBDAM_OUTPUT_COLOR_PATCHDIM=64
CBDAM_OUTPUT_COLOR_FIELD="/data1/cbdam/data/new_formats/quad/color_sardinia_amax_tol"$CBDAM_OUTPUT_COLOR_FIELD_TOLERANCE"_sub"$CBDAM_COLOR_SUBSAMPLING"_alpha"$CBDAM_OUTPUT_COLOR_WAVELET_ALPHA"_patchdim"$CBDAM_OUTPUT_COLOR_PATCHDIM
CBDAM_OUTPUT_COLOR_FIELD_LOG=$CBDAM_OUTPUT_COLOR_FIELD".log"
CBDAM_COLOR_REMAPPING_ALPHA=35
CBDAM_COLOR_REMAPPING_BETA=200

echo "================================================="
echo "STARTING COLOR"
echo "================================================="

../apps/builder/vic_cbdam_builder \
    --one-pass \
    --expected-average-color-data-size 16384 \
    --color \
    --u0 $CBDAM_COLOR_U0 \
    --v0 $CBDAM_COLOR_V0 \
    --u1 $CBDAM_COLOR_U1 \
    --v1 $CBDAM_COLOR_V1 \
    --patch-dim $CBDAM_OUTPUT_COLOR_PATCHDIM \
    --color-remapping-alpha $CBDAM_COLOR_REMAPPING_ALPHA \
    --color-remapping-beta $CBDAM_COLOR_REMAPPING_BETA \
    --subsampling $CBDAM_COLOR_SUBSAMPLING \
    --planar \
    --tolerance $CBDAM_OUTPUT_COLOR_FIELD_TOLERANCE \
    --use-amax-error \
    --wavelet-alpha $CBDAM_OUTPUT_COLOR_WAVELET_ALPHA \
    --output-file $CBDAM_OUTPUT_COLOR_FIELD \
    --tmp-dir $CBDAM_TMP_DIR \
    $CBDAM_INPUT_COLOR_FIELD 2>&1 \
    | tee $CBDAM_OUTPUT_COLOR_FIELD_LOG

echo "================================================="
echo "STARTING HEIGHT"
echo "================================================="

echo ../apps/builder/vic_cbdam_builder \
    --subsampling $CBDAM_HEIGHT_SUBSAMPLING \
    --u0 $CBDAM_HEIGHT_U0 \
    --v0 $CBDAM_HEIGHT_V0 \
    --u1 $CBDAM_HEIGHT_U1 \
    --v1 $CBDAM_HEIGHT_V1 \
    --planar \
    --planar-sample-spacing $CBDAM_INPUT_HEIGHT_FIELD_SAMPLE_SPACING \
    --height-scale $CBDAM_INPUT_HEIGHT_FIELD_HEIGHT_SCALE \
    --patch-dim $CBDAM_OUTPUT_HEIGHT_PATCH_DIM \
    --tolerance $CBDAM_OUTPUT_HEIGHT_FIELD_TOLERANCE \
    --use-amax-error \
    --wavelet-alpha $CBDAM_OUTPUT_HEIGHT_WAVELET_ALPHA \
    --output-file $CBDAM_OUTPUT_HEIGHT_FIELD \
    --tmp-dir $CBDAM_TMP_DIR \
    $CBDAM_INPUT_HEIGHT_FIELD  2>&1 \
    | tee $CBDAM_OUTPUT_HEIGHT_FIELD_LOG

echo "================================================="
echo "FINISHED"
echo "================================================="
